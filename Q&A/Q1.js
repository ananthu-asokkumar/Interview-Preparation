/***
 * 
 * is javascript a statically typed or dynamically typed language?
 * 
 * 
 * The core difference is that statically-typed languages perform type checking at compile time,
 *  while dynamically-typed languages perform type checking at runtime
 * 
 * Dynamic typing
 * 
 * Dynamically-typed languages are those (like JavaScript)
 *  where the interpreter assigns variables a type at runtime based on the variable's value at the time.
 * 
 * 
 * Static typing
 * A statically-typed language is a language (such as Java, C, or C++) where variable types are known at compile time.
 */